#include <iostream>

// Рекурсивная функция, выводящая числа от n до 1
void countdown(int n) {
    // Базовый случай: если n <= 0, прекращаем рекурсию
    if (n <= 0) {
        return;
    }
    
    // Выводим текущее число
    std::cout << n << " ";
    
    // Рекурсивный вызов с n-1
    countdown(n - 1);
}

int main() {
    int n;
    
    // Ввод числа
    std::cout << "Введите число: ";
    std::cin >> n;
    
    // Проверка на корректность ввода
    if (n < 1) {
        std::cout << "Число должно быть положительным." << std::endl;
        return 1;
    }
    
    // Вызов рекурсивной функции
    countdown(n);
    std::cout << std::endl;  // перевод строки в конце
    
    return 0;
}