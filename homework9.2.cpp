#include <iostream>

// Функция, меняющая знак обоих чисел
// Принимает параметры по ссылке (&)
void changeSigns(int &a, int &b) {
    a = -a;  // Меняем знак первого числа
    b = -b;  // Меняем знак второго числа
}

int main() {
    int num1, num2;
    
    // Ввод первого числа
    std::cout << "Введите первое число: ";
    std::cin >> num1;
    
    // Ввод второго числа
    std::cout << "Введите второе число: ";
    std::cin >> num2;
    
    // Вывод исходных значений
    std::cout << "До изменения знаков: " << num1 << " " << num2 << std::endl;
    
    // Вызов функции, которая изменяет знаки чисел
    // Числа передаются по ссылке, поэтому их значения изменятся в функции
    changeSigns(num1, num2);
    
    // Вывод результата
    std::cout << "После изменения знаков: " << num1 << " " << num2 << std::endl;
    
    return 0;
}
